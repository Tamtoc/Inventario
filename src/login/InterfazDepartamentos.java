    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        id = txtID.getText();
        nombre = txtNombre.getText();
        
        if(jefe.equals("")) {
            JOptionPane.showMessageDialog(null, "Seleccione al jefe del departamento");
        } else {
            try {
                PreparedStatement pst = connection.prepareStatement("insert into Jefe (identificador) values(?);");
                pst.setString(1, jefe);
                pst.executeUpdate();
                PreparedStatement pstD = connection.prepareStatement("insert into Departamento (id, nombre, jefe) values(?,?,?);");
                pstD.setString(1, id);
                pstD.setString(2, nombre);
                pstD.setString(3, jefe);
                pstD.executeUpdate();
                JOptionPane.showMessageDialog(null, "Departamento registrado correctamente");
                this.setVisible(false);
            } catch (SQLException ex) {
                System.out.println(ex.toString());
            }
        }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

